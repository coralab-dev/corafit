import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const scriptDirectory = dirname(fileURLToPath(import.meta.url));
const webRoot = resolve(scriptDirectory, "..");
const outputPath = join(webRoot, "components/client-portal/completion-card-brand.ts");
const sources = {
  dark: join(webRoot, "public/brand/imagotipo-texto-blanco.svg"),
  light: join(webRoot, "public/brand/imagotipo-texto-negro.svg"),
};

const dataUris = Object.fromEntries(
  Object.entries(sources).map(([theme, sourcePath]) => [
    theme,
    `data:image/svg+xml;base64,${readFileSync(sourcePath).toString("base64")}`,
  ]),
);

const generatedFile = `// Generated by apps/web/scripts/generate-completion-card-brand.mjs.
// Sources: apps/web/public/brand/imagotipo-texto-negro.svg and imagotipo-texto-blanco.svg.

const completionCardBrandDataUris = {
  dark: ${JSON.stringify(dataUris.dark)},
  light: ${JSON.stringify(dataUris.light)},
} as const;

export function getCompletionCardBrandDataUri(dark: boolean) {
  return dark
    ? completionCardBrandDataUris.dark
    : completionCardBrandDataUris.light;
}
`;

writeFileSync(outputPath, generatedFile, "utf8");
